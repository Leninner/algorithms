/**
 *
 * @param bills - An array of integers representing the amount of money paid (5, 10, 20)
 * @returns - True if the amount of money paid is enough to buy lemonade, false otherwise
 * @link https://leetcode.com/problems/lemonade-change/
 */

function lemonadeChange(bills: number[]): any {
  let acc: number[] = [];

  for (let i = 0; i < bills.length; i++) {
    if (bills[i] > 5) {
      for (let k = 0; k < acc.length; k++) {
        for (let j = k; j < acc.length; j++) {
          if (acc[k] + acc[j] === bills[i] - 5) {
            return true;
          }

          acc.unshift();
        }
      }
    }

    acc.push(bills[i]);
  }

  return false;
}

